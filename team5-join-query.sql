/*Get all make/model cars where we made a profit*/

SELECT CAM13052.INVENTORY.PRODUCT_NAME
FROM CAM13052.INVENTORY INNER JOIN CAM13052.SALE ON CAM13052.INVENTORY.PRODUCT_ID=CAM13052.SALE.PRODUCT_ID
WHERE CAM13052.SALE.PROFIT>0;

/*Get all customers that spent over user specified amount*/

SELECT CAM13052.TEAM5_CUSTOMER.CUSTOMER_ID
FROM CAM13052.TEAM5_CUSTOMER INNER JOIN CAM13052.SALE 
ON CAM13052.TEAM5_CUSTOMER.CUSTOMER_ID=CAM13052.SALE.CUSTOMER_ID
WHERE CAM13052.SALE.SALE_PRICE>&SALE_PRICE;

/*Get purchase cost, repair cost, and make/model for cars. Order by repair cost*/

SELECT CAM13052.PURCHASE.COST AS "Purchase Cost", CAM13052.REPAIR.COST AS "Repair Cost", CAM13052.INVENTORY.MAKE, CAM13052.INVENTORY.MODEL
FROM CAM13052.PURCHASE INNER JOIN CAM13052.REPAIR
ON CAM13052.PURCHASE.PRODUCT_ID=CAM13052.REPAIR.PRODUCT_ID
INNER JOIN CAM13052.INVENTORY
ON CAM13052.PURCHASE.PRODUCT_ID=CAM13052.INVENTORY.PRODUCT_ID
ORDER BY CAM13052.REPAIR.COST DESC;

/*Get First, last name, nationality of customers. Get make/model of cars they bought Order by nationality*/
SELECT CAM13052.TEAM5_CUSTOMER.FIRST_NAME, CAM13052.TEAM5_CUSTOMER.LAST_NAME, 
CAM13052.TEAM5_CUSTOMER.NATIONALITY, CAM13052.INVENTORY.MAKE, CAM13052.INVENTORY.MODEL
FROM CAM13052.TEAM5_CUSTOMER INNER JOIN CAM13052.SALE
ON CAM13052.TEAM5_CUSTOMER.CUSTOMER_ID=CAM13052.SALE.CUSTOMER_ID
INNER JOIN CAM13052.INVENTORY
ON CAM13052.SALE.PRODUCT_ID=CAM13052.INVENTORY.PRODUCT_ID
ORDER BY CAM13052.TEAM5_CUSTOMER.NATIONALITY DESC;


